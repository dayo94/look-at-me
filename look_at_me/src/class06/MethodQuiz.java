package class06;

public class MethodQuiz {

	


	public void print100() {
		for(int i=1; i<101; i++) {
			System.out.println(i);
		}
	}
	
	
	
	

	
	public int printHello( int num ) {
		for(int i=0; i<num; i++) {
			System.out.println("Hello");
		}
		return num;
	}
	

	
	public String printText(int cnt, String text) {
		
		for(int i=0; i<cnt; i++) {
			System.out.println(text);
		}
		return text;
	}

	
	
	
	
	
	
	
	
	
	
	
}//class
